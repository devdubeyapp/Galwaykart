package com.galwaykart.helpdesksupport;

public class CompCategModel {

    String complaint_category_id;
    String title;
    String status;
    String position;
    String days;
    String match_code;
    String show;

    String request_type;



    public String getComplaintCategory_id() {
        return complaint_category_id;
    }

    public void setComplaintCategory_id(String complaint_category_id) {
        this.complaint_category_id = complaint_category_id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getDays() {
        return days;
    }

    public void setDays(String days) {
        this.days = days;
    }

    public String getMatch_code() {
        return match_code;
    }

    public void setMatch_code(String match_code) {
        this.match_code = match_code;
    }

    public String getShow() {
        return show;
    }

    public void setShow(String show) {
        this.show = show;
    }


    public String getRequest_type() {
        return request_type;
    }

    public void setRequest_type(String request_type) {
        this.request_type = request_type;
    }


}
